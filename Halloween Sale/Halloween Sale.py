# Complete the howManyGames function below.
def howManyGames(p, d, m, s):
    res=0
    money=0
    while money<=s:
        res+=1
        money+=p
        if p-d>m:
            p-=d
        else: p=m
    return res-1
    
if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    pdms = input().split()

    p = int(pdms[0])

    d = int(pdms[1])

    m = int(pdms[2])

    s = int(pdms[3])

    answer = howManyGames(p, d, m, s)

    fptr.write(str(answer) + '\n')

    fptr.close()
